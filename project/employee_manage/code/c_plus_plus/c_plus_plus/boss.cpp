#include "boss.h"

// 子类实现
Boss::Boss(int id, string name, int dID)
{
	this->w_ID = id;
	this->w_Name = name;
	this->w_DeptID = dID;
}

void Boss::showInfo()
{
	cout << "员工编号: " << this->w_ID
		<< "\t员工姓名: " << this->w_Name
		<< "\t岗位: " << this->getDeptName()
		<< "\t 岗位职责: 管理公司所有事务" << endl;
}

string Boss::getDeptName()
{
	return string("老板");
}